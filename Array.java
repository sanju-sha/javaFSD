
public class Array 
{
 public static void main(String[]args)
 {
	 int a[][]=new int[2][2];
	 a[0][0]=26;
	 a[0][1]=74;
	 a[1][0]=32;
	 a[1][1]=67;
	 for(int i=0;i<2;i++)
	 {
		 for(int j=0;j<2;j++)
		 {
			  System.out.print(a[i][j]+"\t");
		 }
		 System.out.println("\n");
	 }
 }
}  
		 
	 
		
	 
				

	 
 

